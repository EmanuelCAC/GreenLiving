                    <div class="sub_titulo">
                        <h1>Porque devemos ter práticas sustentáveis?</h1>
                    </div>
                    <div class="texto">
                        <p class="paragrafo">O livro "Bioética" pelo bioquímico Van Renssenlaer Petter, evidencia a responsabilidade da 
                            intervenção do homem sobre a vida, alegando que ações que diminuem as chances de sobrevivência humana são 
                            imorais. Para além da obra, observa-se que, na conjuntura brasileira contemporânea, é fundamental que seja 
                            estabelecida um desenvolvimento sustentável, com o objetivo de estimular práricas que sejam nocivasao meio ambiente. 
                            Nesse viés, torna-se crucial analisar as causas da insustentabilidade, dentre as quais se destacamos hábitos maléficos 
                            à natureza e a ausência de responsabilidade social. 
                        </p>
                        <p class="paragrafo">A princípio, é imperioso notar que as práticas nocivas ao meio ambiente potencializam o problema. 
                            Posto isso, o estudo "Poluentes Tóxicos do lixo Plástico Uma revisão", realizado pela University of Agricultural Sciences, 
                            Bangalare e College of Sericulture, Chintamani, aponta que, em média, 12% dos resíduos sólidosmunicipais são compostos de plásticos, 
                            que são queimados, liberando diversos gases tóxicos na atmosfera. Cabendo citar o PVC, que libera o ácido clorídrico, um gás prejudicial ao meio ambiente. 
                            Dessa forma, faz-se imprescíndivel a abstenção da queima de lixos.
                        </p>
                        <p class="paragrafo">Outrossim, é fundamental destacar que a irresponsabilidade social intensifica a recorrenência de ações insustentáveis.
                            De acordo com o relatório "Estado do Mundo - 2010", realizado pelo Instituto Akatu e Worldwatch Institute, nos últimos
                            anos o consumo mundial de bens e serviços teve um drástico aumento, cerca de 28% maior que o de uma década atrás. Diante
                            de tal exposto, é crucial observar o aumento desenfreado do consumismo, pois este traz muitos danos à natureza, uma vez que 
                            para produzir tantos bens é preciso usar cada vez mais recursos naturais. Logo, é de extrema importância frear o consumo exagerado.
                        </p>
                        <p class="paragrafo">Portanto, a prática sustentável é indispensável no mundo contemporâneo. Isso porque a adoção de hábitos 
                            sustentáveis proporciona condições mínimas para sobrevivência humana, a qual depende da criação de um ambiente sustentável. 
                            Esta, por sua vez, foge de ações prejudiciais ao meio ambiente e forma cidadãos mais responsáveis socialmente. Assim, o mundo
                            se aproxima de uma sociedade ideal e se afasta da imoralidade discutida no livro "Bioética".
                        </p>
